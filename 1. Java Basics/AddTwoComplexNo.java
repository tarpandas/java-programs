public class AddTwoComplexNo {
    public static void main(String[] args) {
        int iNum1 = 34;
        int iComplex1 = 34;
        int iNum2 = 12;
        int iComplex2 = 8;
        System.out.println("No. 1: "+iNum1+" + "+iComplex1+"i");
        System.out.println("No. 2: "+iNum2+" + "+iComplex2+"i");

        int iSumNum = iNum1 + iNum2;
        int iSumComplex = iComplex1 + iComplex2;
        System.out.println("Sum: "+iSumNum+" + "+iSumComplex+"i");
    }
}

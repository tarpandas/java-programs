public class QuotientAndRemainder {
    public static void main(String[] args) {
        int iNum1 = 34535;
        int iNum2 = 34;

        int iQuotient = iNum1 / iNum2;
        int iRemainder = iNum1 % iNum2;

        System.out.println("Quotient: "+ iQuotient+ "\nRemainder: "+ iRemainder);
    }
}
